package com.codegym.task.task03.task0314;

/* 
Multiplication table

*/

public class Solution {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i + " " + i*2 + " " + i*3 + " " + i*4 + " " + i*5 + " " + i*6 + " " + i*7 + " " + i*8 + " " + i*9 + " " + i*10);
            i++;
        }
    }
}
